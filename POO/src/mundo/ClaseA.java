package mundo;

public abstract class ClaseA {

	private int x , y;
	
	//constructor
	public ClaseA(int x, int y) {
		this.x = x;
		this.y = y;
	}
	
	public ClaseA() {
		
	}
	
	//get & set 
	
	public int getX() {return x;}

	public void setX(int x) {this.x = x;}

	public int getY() {	return y;}

	public void setY(int y) {this.y = y;}
	
	
}
